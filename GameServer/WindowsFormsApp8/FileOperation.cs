    public static void Delete(string content)
    {
        List<string> list = File.ReadAllLines(Path, Encoding.Default).ToList();
        for (int i = 0; i < list.Count; i++)
        {
            if (list[i] == content)
            {
                list.RemoveAt(i);
                break;
            }
        }
        File.WriteAllLines(Path, list, Encoding.Default);
    }
